# Defining my string variable

string = "gemma loves PIZZA"
puts "The string I will use to show text manipulation will be...[#{string}] "
# Converting the string to uppercase
string1 = string.upcase!
puts string1
# Capitalising the first letter of the string
string2 = string.capitalize!
puts string2
# Swapping the case of any given character to the opposite of its original
string3 = string.swapcase!
puts string3
# Reversing the string
string4 = string.reverse!
puts string4
